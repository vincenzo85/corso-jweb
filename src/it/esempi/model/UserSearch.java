package it.esempi.model;

public class UserSearch {
	
	private String pagenumb;
	private String search;
	private String orderby;
	private String rememberPar;
	public String getPagenumb() {
		return pagenumb;
	}
	public void setPagenumb(String pagenumb) {
		this.pagenumb = pagenumb;
	}
	public String getSearch() {
		return search;
	}
	public void setSearch(String search) {
		this.search = search;
	}
	public String getOrderby() {
		return orderby;
	}
	public void setOrderby(String orderby) {
		this.orderby = orderby;
	}
	public String getRememberPar() {
		return rememberPar;
	}
	public void setRememberPar(String rememberPar) {
		this.rememberPar = rememberPar;
	}
	

}
